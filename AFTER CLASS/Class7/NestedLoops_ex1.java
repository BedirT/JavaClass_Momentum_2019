
public class NestedLoops_ex1 {

	public static void main(String[] args) {

		// Create the shape using nested loops:
		// 		******
		//		******
		//		******
		//		******
		//		******
		//		******
		
		for(int i = 0; i < 6; i++) {
			for(int j = 0; j < 6; j++) {
				System.out.print("*");
			}
			System.out.println("");
		}
		
	}

}
